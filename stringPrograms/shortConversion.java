class shortConversion
{
	public static void main(String [] args)
	{
		short s=5;
		System.out.println("short to int:"+s);
		//char c=a;
		//System.out.println("byte to char:" +c);
		int i=s;
		System.out.println("short to long:"+i);
		long l=s;
		System.out.println("short to float:"+l);
		float f=s;
		System.out.println("short to double:"+f);
		double d=s;
		System.out.println("byte to double:"+d);
	}
}