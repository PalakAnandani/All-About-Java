class intConversion
{
	public static void main(String [] args)
	{
		int i=15;
		System.out.println("int value:"+i);
		long l =i;
		System.out.println("int to long:"+l);
		//char c=a;
		//System.out.println("byte to char:" +c);
		float f=i;
		System.out.println("int to float:"+f);
		double d=i;
		System.out.println("int to double:"+d);
	}
}